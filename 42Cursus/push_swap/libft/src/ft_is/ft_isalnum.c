#include "../../inc/libft.h"

int	ft_isalnum(int n)
{
	if ((n >= 'A' && n <= 'Z') || (n >= 'a' && n <= 'z')
		|| (n >= 48 && n <= 57))
		return (1);
	else
		return (0);
}
