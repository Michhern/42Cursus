#include "../../inc/libft.h"

void	ft_putendl_fd(char *str, int fd)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		write (fd, &str[c], 1);
		c++;
	}
	write (fd, "\n", 1);
}
