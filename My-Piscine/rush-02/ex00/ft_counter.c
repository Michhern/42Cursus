int	ft_counter(char *str)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	while(str[i] != '\0')
	{
		if (str[count] == "\n" && str[count + 1] = "\n")
			count += 1;
	}
	i++;
}
